#!/usr/bin/python3

# Typecasting is the ability to convert the data type of a value to another data type.
x = 1
y = 2.0
z = "3"
print("x: "+str(type(x)))
print("y: "+str(type(y)))
print("z: "+str(type(z)))
# convert y and z to integer
y = int(y)
z = int(z)
print("after typecasting")
print("y: "+str(type(y)))
print("z: "+str(type(z)))
